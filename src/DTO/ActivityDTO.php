<?php

namespace App\DTO;

use Symfony\Component\HttpFoundation\Request;

class ActivityDTO extends Request
{
    private string $name;

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName(string $name): void
    {
        $this->name = $name;
    }
}